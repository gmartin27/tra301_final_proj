def evaluate_at_threshold(test_df, threshold_value):
    total_actual = 0
    total_pred = 0
    exact_matches = 0

    for _, row in test_df.iterrows():
        raw_actual = row['HUMAN_TRUTH']
        raw_predicted = extract_full_indicators(row['Clue'], threshold=threshold_value)
        raw_predicted = raw_predicted if isinstance(raw_predicted, dict) else {}

        actual = {normalize_text(word): tag for word, tag in raw_actual.items()}
        predicted = {normalize_text(word): tag for word, tag in raw_predicted.items()}

        total_actual += len(actual)
        total_pred += len(predicted)

        for p_word, p_type in predicted.items():
            if p_word in actual and p_type == actual[p_word]:
                exact_matches += 1

    precision = (exact_matches / total_pred) * 100 if total_pred > 0 else 0
    recall = (exact_matches / total_actual) * 100 if total_actual > 0 else 0
    f1 = (2 * precision * recall) / (precision + recall) if (precision + recall) > 0 else 0

    return precision, recall, f1, total_pred

