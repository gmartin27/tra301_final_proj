# threshold from table above
THRESHOLD = 0.5

false_negatives = []
false_positives = []
type_mismatches = []

for _, row in eval_df.iterrows():
    clue = str(row['Clue'])

    raw_human_truth = row['HUMAN_TRUTH'] if isinstance(row['HUMAN_TRUTH'], dict) else {}
    human_truth = {normalize_text(word): tag for word, tag in raw_human_truth.items()}

    # This now calls our hyper-clean pipeline wrapper!
    raw_preds = extract_full_indicators(clue, threshold=THRESHOLD)
    raw_preds = raw_preds if isinstance(raw_preds, dict) else {}

    ai_preds = {normalize_text(word): tag for word, tag in raw_preds.items()}

    for ai_word, ai_type in ai_preds.items():
        if ai_word not in human_truth:
            false_positives.append({"clue": clue, "word": ai_word, "ai_guessed": ai_type})
        elif human_truth[ai_word] != ai_type:
            type_mismatches.append({
                "clue": clue,
                "word": ai_word,
                "ai_guessed": ai_type,
                "human_truth": human_truth[ai_word]
            })

    for human_word, human_type in human_truth.items():
        if human_word not in ai_preds:
            false_negatives.append({"clue": clue, "word": human_word, "human_truth": human_type})

print("="*50)
