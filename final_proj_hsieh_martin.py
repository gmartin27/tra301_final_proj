def create_bio_tags(row):
    clue = str(row['Clue']).lower()
    words = re.findall(r"[\w']+|[.,!?;]", clue)

    tags = ['O'] * len(words)
    mapped_inds = row.get('Mapped_Indicators', {})

    if not isinstance(mapped_inds, dict) or len(mapped_inds) == 0:
        return {"words": words, "tags": tags}

    for ind_phrase, ind_type in mapped_inds.items():
        ind_words = re.findall(r"[\w']+|[.,!?;]", ind_phrase.lower())
        ind_len = len(ind_words)

        for i in range(len(words) - ind_len + 1):
            if words[i:i+ind_len] == ind_words:
                tags[i] = f"B-{ind_type}"
                for j in range(1, ind_len):
                    tags[i+j] = f"I-{ind_type}"

    return {"words": words, "tags": tags}

