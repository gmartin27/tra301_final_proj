def fetch_fandom_api(page_slug):
    """Fetches indicator words from the Fandom Wiki using their MediaWiki API."""
    print(f"Fetching {page_slug} via API...")

    params = {
        'action': 'parse',
        'page': page_slug,
        'format': 'json',
        'prop': 'text'
    }

    headers = {
        'User-Agent': 'MinuteCrypticResearchBot/1.0 (Data Science Project) Python/Requests',
        'Accept': 'application/json'
    }

    indicators = []

    try:
        response = requests.get(WIKI_API_URL, params=params, headers=headers)
        response.raise_for_status()
        data = response.json()

        if 'parse' in data and 'text' in data['parse']:
            html_content = data['parse']['text']['*']
            soup = BeautifulSoup(html_content, 'html.parser')

            for li in soup.find_all('li'):
                text = li.get_text(strip=True)

                text = re.sub(r'\[\d+\]', '', text)
                text = text.replace('*', '').strip().lower()

                if 0 < len(text) < 35 and ':' not in text and 'list of' not in text and '^' not in text:
                    indicators.append(text)

        return list(set(indicators))

    except requests.exceptions.RequestException as e:
        print(f"Error fetching {page_slug}: {e}")
        return []


