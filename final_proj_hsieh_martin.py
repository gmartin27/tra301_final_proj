class WeightedNERTrainer(Trainer):
    def compute_loss(self, model, inputs, return_outputs=False, **kwargs):
        # Extract the labels and get the model's predictions
        labels = inputs.pop("labels")
        outputs = model(**inputs)
        logits = outputs.logits

        # Create weights for each class: default is 1.0
        num_labels = model.config.num_labels
        weights = pytorch_class_weights.to(model.device)

        # Apply the weighted loss function
        loss_fct = nn.CrossEntropyLoss(weight=weights)

        active_loss = labels.view(-1) != -100
        active_logits = logits.view(-1, num_labels)[active_loss]
        active_labels = labels.view(-1)[active_loss]

        loss = loss_fct(active_logits, active_labels)

        return (loss, outputs) if return_outputs else loss

