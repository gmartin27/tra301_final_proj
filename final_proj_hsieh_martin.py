def augment_clue(row, num_augments=5):
    clue = str(row['Clue']).lower()
    mapped_inds = row.get('Mapped_Indicators', {})

    if not isinstance(mapped_inds, dict) or len(mapped_inds) == 0:
        return [row.to_dict()]

    words = re.findall(r"[\w']+|[.,!?;]", clue)

    ind_indices = set()
    for ind_phrase in mapped_inds.keys():
        ind_words = re.findall(r"[\w']+|[.,!?;]", ind_phrase.lower())
        ind_len = len(ind_words)
        for i in range(len(words) - ind_len + 1):
            if words[i:i+ind_len] == ind_words:
                for j in range(ind_len):
                    ind_indices.add(i+j)

    augmented_rows = [row.to_dict()]

    for _ in range(num_augments):
        new_words = []
        for i, word in enumerate(words):
            if i in ind_indices or not word.isalpha():
                new_words.append(word)
            else:
                if random.random() < 0.50:
                    new_words.append(random.choice(safe_vocab))
                else:
                    new_words.append(word)

        new_clue = " ".join(new_words).replace(" ,", ",").replace(" .", ".").replace(" !", "!").replace(" ?", "?")

        new_row = row.to_dict()
        new_row['Clue'] = new_clue
        augmented_rows.append(new_row)

    return augmented_rows

